"""Run the AAAI-oriented ChemWorld experiment package."""

from __future__ import annotations

import argparse
import json
from pathlib import Path

from chemworld.eval.baseline_report import AAAI_BASELINE_AGENTS, generate_baseline_report
from chemworld.eval.paper_artifact import create_paper_artifact
from chemworld.tasks import AAAI_TASK_IDS


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--output-dir", default="runs/aaai_2027")
    parser.add_argument("--smoke", action="store_true", help="Run a small fast seed/agent slice.")
    parser.add_argument("--tasks", nargs="+")
    parser.add_argument("--agents", nargs="+")
    parser.add_argument("--seeds", nargs="+", type=int)
    parser.add_argument(
        "--codex-trace-dir",
        help=(
            "Optional directory containing externally orchestrated Codex sub-agent "
            "JSONL traces. The script records the directory in the run manifest; "
            "trace generation itself is done outside repository code."
        ),
    )
    args = parser.parse_args()

    root = Path(args.output_dir)
    root.mkdir(parents=True, exist_ok=True)
    tasks = args.tasks or list(AAAI_TASK_IDS)
    if args.smoke:
        agents = args.agents or ["scripted_chemistry", "tool_using_llm_stub"]
        seeds = args.seeds or [0]
    else:
        agents = args.agents or list(AAAI_BASELINE_AGENTS)
        seeds = args.seeds

    baseline_report = generate_baseline_report(
        task_ids=tasks,
        agents=agents,
        seeds=seeds,
        output_dir=root / "baseline_report",
    )
    artifact_summary = create_paper_artifact(
        output_dir=root / "artifact",
        task_ids=tasks,
        agents=agents,
        seeds=seeds or [0],
    )
    manifest = {
        "schema_version": "chemworld-aaai-experiment-run-0.1",
        "tasks": tasks,
        "agents": agents,
        "seeds": seeds,
        "smoke": bool(args.smoke),
        "baseline_report": str(root / "baseline_report" / "baseline_report.json"),
        "artifact_summary": str(root / "artifact" / "artifact_summary.json"),
        "codex_trace_dir": args.codex_trace_dir,
        "codex_online_policy": (
            "codex_subagent_online traces are generated by the external evaluator "
            "session, saved as JSONL, and replayed through codex_subagent_replay."
        ),
        "baseline_result_count": baseline_report.result_count,
        "artifact_replay_verified": artifact_summary["replay_verified"],
    }
    (root / "aaai_experiment_manifest.json").write_text(
        json.dumps(manifest, indent=2, sort_keys=True),
        encoding="utf-8",
    )
    print(json.dumps(manifest, indent=2, sort_keys=True))


if __name__ == "__main__":
    main()
